package com.walrusone.skywars.utilities;

public class EmptyChest {
	private int x;
	private int z;
	private int y;
	
	public EmptyChest(int x, int y, int z) {
		this.x = x;
		this.z = z;
		this.y = y;
	}
	
	public int getX() {
		return x;
	}

	public int getZ() {
		return z;
	}
	
	public int getY() {
		return y;
	}
	
}
